public class Numeros {
    public static void main(String[] args) {
        // Impressão dos números de 1 a 20 um abaixo do outro
        System.out.println("Números de 1 a 20 um abaixo do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        // Impressão dos números de 1 a 20 um ao lado do outro
        System.out.println("\nNúmeros de 1 a 20 um ao lado do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}
